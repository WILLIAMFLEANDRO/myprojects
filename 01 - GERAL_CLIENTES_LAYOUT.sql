SELECT  DISTINCT
		D2.CODCLI as CODIGO_SARWIN_CLI,
		D3.CODCLI as CODIGO_SARWIN_SF,
		D1.TIPO_PESSOA AS A1_PESSOA,
		SUBSTRING (D1.CGC,1,15) as A1_CGC,  
		SUBSTRING (D1.CGC, 1 , 8 ) as A1_CODIGO, 
		SUBSTRING (D1.CGC, 11 , 4 ) as A1_LOJA,
		D2.INSCEST as A1_INSCR,
		D3.INSCEST as A1_INSCR_SF,
		D2.NOMECLI as A1_NOME,
		D3.NOMESACADO as A1_NREDUZ,
		D2.PRACAPAG as A1_END,
		D3.LOCPAG as A1_END_2,
		D2.BAIRROPAG as A1_BAIRRO,
		D3.BAIRROPAG as A1_BAIRRO_2,
		D2.ESTADOPAG as A1_EST,
		D3.ESTADOPAG as A1_EST_2,
		D2.CEPPAG as A1_CEP,
		D3.CEPPAG as A1_CEP,
		--D4.IBGE as A1_COD_MUN,
		D2.MUNICIPPAG as A1_MUN,
		D3.MUNICIPPAG as A1_MUN
		
FROM CNPJ_CLIENTES_GERAL D1 
LEFT JOIN SARCLI_GERAL D2 ON D1.CGC = D2.CGC 
LEFT JOIN SFGRUPO_GERAL D3 ON D1.CGC = D3.CGC 
LEFT JOIN SARCIDADES_GERAL D4 ON D4.CODCID = D3.CODCID
WHERE D1.CGC = '01701201018550'


